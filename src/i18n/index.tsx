export type Translate = (key: MessageKey, params?: MessageParams) => string

export function translate(locale: Locale, key: MessageKey, params?: MessageParams): string {
  const dictionary = locale === 'en-US' ? en : zh
  const template = dictionary[key]
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (match, name: string) => (
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match
  ))
}

interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: Translate
}

const I18nContext = createContext<I18nContextValue>({
  locale: 'zh-CN',
  setLocale: () => undefined,
  t: (key, params) => translate('zh-CN', key, params),
})

const STORAGE_KEY = 'ultigrid.locale'

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    if (typeof window === 'undefined') return 'zh-CN'
    const stored = window.localStorage.getItem(STORAGE_KEY)
    return stored === 'en-US' || stored === 'zh-CN' ? stored : 'zh-CN'
  })

  useEffect(() => {
    document.documentElement.lang = locale
    window.localStorage.setItem(STORAGE_KEY, locale)
  }, [locale])

  const t = useCallback<Translate>((key, params) => translate(locale, key, params), [locale])
  const value = useMemo(() => ({ locale, setLocale, t }), [locale, t])
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext)
}
